    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        Clientes c = new Clientes();
        Carro c1= new Carro();
        c1.setVeiculo(txt_veiculo.getText());
        c1.setAno(txt_ano.getText());
        c1.setCor(txt_cor.getText());
        c1.setPlaca(txt_placa.getText());
        c.setNome(txt_nome.getText());
        c.setRua(txt_rua.getText());
        c.setBairro(txt_bairro.getText());
        c.setCep(txt_cep.getText());
        c.setNumerocasa(txt_numero.getText());
        c.setCpfcnpj(txt_cpf.getText());
        c.setTelefone(txt_telefone.getText());
        if(c.gravar()){
            JOptionPane.showMessageDialog(rootPane, "Sucesso ao gravar Cliente");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Erro ao gravar Cliente");
        }
        if(c1.gravarc())
        {
            JOptionPane.showMessageDialog(rootPane, "Sucesso ao gravar Carro");
        }else{
             JOptionPane.showMessageDialog(rootPane, "Erro ao gravar Carro");
        }
    }//GEN-LAST:event_btn_cadastrarActionPerformed


